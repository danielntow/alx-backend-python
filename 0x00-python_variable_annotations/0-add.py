#!/usr/bin/env python3
"""
Module: arithmetic

This module provides basic arithmetic functions.
"""


def add(a: float, b: float) -> float:
    """
    Add two numbers and return the sum.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
